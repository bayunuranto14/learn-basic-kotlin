//For untuk melakukan perulanghan ke data iterator -> Array, Range
//While perulangan sangat flexible, dimana kode akan melakukan pengecekan kondisi sampai bernilai false

fun main() {
    var i = 0
    while (i<10) {
        println("Perulangan $i")
        i++
    }
    println("Selesai Perulangan")

}